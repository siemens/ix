import { testIds as HTMLPreviewTestIds } from './generated/test-ids.autogenerated';
import { excludedTestIds } from './exclude-test-ids';

// Preview examples does not exist for html-test-app
const additionalTestIds = ['form-validation'] as const;
const allTestIds = [...HTMLPreviewTestIds, ...additionalTestIds] as const;

type ExcludedTestIds = (typeof excludedTestIds)[number];
type FilteredTestIds = Exclude<(typeof allTestIds)[number], ExcludedTestIds>;
const testIds: readonly FilteredTestIds[] = allTestIds.filter(
  (id): id is FilteredTestIds =>
    !(excludedTestIds as readonly string[]).includes(id)
);

export { HTMLPreviewTestIds, testIds, excludedTestIds };
